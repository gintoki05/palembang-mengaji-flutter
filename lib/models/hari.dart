  final String image;
  final String tanggal;
  final String masjid;
  final String waktu;
  final String pengisi;
  final String tema;
  final String kategori;

  Hari(
      {@required this.image,
      @required this.tanggal,
      @required this.masjid,
      @required this.waktu,
      @required this.pengisi,
      @required this.tema,
      @required this.kategori});
}
